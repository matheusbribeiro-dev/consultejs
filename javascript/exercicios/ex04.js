const divisorDividendo = (divisor, dividendo) => {
  console.log('--------------')
  console.log(`Quociente(${dividendo+'/'+divisor}): ${dividendo/divisor}`)
  console.log(`Resto(${dividendo+'/'+divisor}): ${dividendo%divisor}`)
  console.log('--------------')
}

divisorDividendo(2, 10)
divisorDividendo(5, 10)
divisorDividendo(3, 7)
